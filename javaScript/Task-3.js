// Check whether a string contains all the vowels a, e, i, o, u
let name = "kamlauddin";

if (name.toLowerCase().includes("a") && name.toLowerCase().includes("e") && name.toLowerCase().includes("i") && name.toLowerCase().includes("o") && name.toLowerCase().includes("u")) {
    console.log("true");
} else {
    console.log("false");
}
